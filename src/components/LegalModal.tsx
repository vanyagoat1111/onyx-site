export default function LegalModal() {
  const [openSection, setOpenSection] = useState<string | null>(null);
  const [expandedIndex, setExpandedIndex] = useState<number | null>(0);

  useEffect(() => {
    const handleOpen = (e: Event) => {
      const customEvent = e as CustomEvent<string>;
      setOpenSection(customEvent.detail);
      setExpandedIndex(0); // Open the first section by default
    };

    document.addEventListener('open-legal', handleOpen);
    return () => document.removeEventListener('open-legal', handleOpen);
  }, []);

  if (!openSection) return null;

  const doc = legalDocs[openSection];
  if (!doc) return null;

  return (
    <div 
      className="fixed inset-0 z-[100] bg-onyx-950/90 backdrop-blur-md overflow-y-auto"
      onClick={() => setOpenSection(null)}
    >
      <div className="min-h-full p-4 py-16 sm:p-8 md:py-24 flex items-start justify-center">
        <div 
          className="relative w-full max-w-4xl bg-onyx-900 border border-blue-400 p-6 sm:p-10 shadow-[0_0_50px_rgba(147,197,253,0.2)] m-auto"
          onClick={(e) => e.stopPropagation()}
        >
          <button 
            onClick={() => setOpenSection(null)} 
            className="absolute top-4 right-4 sm:top-6 sm:right-6 text-blue-300 hover:text-blue-500 transition-colors drop-shadow-[0_0_5px_currentColor] z-10"
          >
            <X size={32} strokeWidth={1} />
          </button>
          
          <h2 className="text-xl sm:text-2xl md:text-3xl font-black text-white uppercase tracking-wide mb-8 pr-10">{doc.title}</h2>
          
          <div className="space-y-4">
            {doc.sections.map((section, idx) => {
              const isOpen = expandedIndex === idx;
              return (
                <div key={idx} className="border border-onyx-800 bg-onyx-950/50">
                  <button
                    onClick={() => setExpandedIndex(isOpen ? null : idx)}
                    className="w-full text-left px-6 py-4 flex justify-between items-center hover:bg-onyx-800 transition-colors"
                  >
                    <span className="font-bold text-white text-sm sm:text-base">{section.title}</span>
                    <ChevronDown className={`w-5 h-5 text-blue-500 transition-transform duration-300 ${isOpen ? 'rotate-180' : ''}`} />
                  </button>
                  <div className={`overflow-hidden transition-all duration-300 ${isOpen ? 'max-h-[1000px] opacity-100' : 'max-h-0 opacity-0'}`}>
                    <div className="p-6 pt-0 text-neutral-400 font-mono text-sm leading-relaxed whitespace-pre-line">
                      {section.content}
                    </div>
                  </div>
                </div>
              );
            })}
          </div>

          <div className="mt-8 pt-6 border-t border-onyx-800 text-xs text-neutral-500">
            Актуальная редакция от 2026 года. Самозанятый Новиков И. М.
          </div>
        </div>
      </div>
    </div>
  );
}
